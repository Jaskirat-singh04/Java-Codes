// finding the average of an array
public class arrays1 {
    public static void main(String args[]){
         double num[]={10.1, 13.4,14.5,113.55, 19,7};
         double result=0;

         for(int i=0; i<5;i++){
             result=result+num[i];
             System.out.println("avg is: "+result);
         }
    }
    
}
